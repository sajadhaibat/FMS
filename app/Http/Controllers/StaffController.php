     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $staff = new Staff;
        $staff->name=$request->name;
        $staff->number=$request->number;
        $staff->save();
        return redirect(route('staff.index'));
    }

